// returns number of characters
int readln(uint8_t message[])
{
  int i = 0;
  do 
  {
    HAL_UART_Receive(&huart2, &message[i], 1, 0xFFFF);
    i++;
  } while (message[i-1] != (uint8_t) 0xD);
  message[i] = '\0';
  return i;
}

double atof(uint8_t* num)
 {
     if (!num || !*num)
         return 0; 
     double integerPart = 0;
     double fractionPart = 0;
     int divisorForFraction = 1;
     int sign = 1;
     int inFraction = 0;
     /*Take care of +/- sign*/
     if (*num == '-')
     {
         ++num;
         sign = -1;
     }
     else if (*num == '+')
     {
         ++num;
     }
     while (*num != '\0')
     {
         if (*num >= '0' && *num <= '9')
         {
             if (inFraction)
             {
                 /*See how are we converting a character to integer*/
                 fractionPart = fractionPart*10 + (*num - '0');
                 divisorForFraction *= 10;
             }
             else
             {
                 integerPart = integerPart*10 + (*num - '0');
             }
         }
         else if (*num == '.')
         {
             if (inFraction)
                 return sign * (integerPart + fractionPart/divisorForFraction);
             else
                 inFraction = 1;
         }
         else
         {
             return sign * (integerPart + fractionPart/divisorForFraction);
         }
         ++num;
     }
     return sign * (integerPart + fractionPart/divisorForFraction);
 }

/**
 * Look for character /code/ in the buffer and read the float that immediately follows it.
 * @return the value found.  If nothing is found, /val/ is returned.
 * @input code the character to look for.
 * @input val the return value if /code/ is not found.
 **/
double parseNumber(uint8_t code,float val) {
  uint8_t *ptr=message;  // start at the beginning of buffer
  while((long)ptr > 1 && (*ptr) && (long)ptr < (long)message+len-1) {  // walk to the end
    if(*ptr==code) {  // if you find code on your walk,
      return atof(ptr+1);  // convert the digits that follow into a float and return it
    }
    ptr=ptr+1;  // take a step from here to the letter after the next space
  }
  return val;  // end reached, nothing found, return default val.
}

